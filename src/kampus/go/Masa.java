public class Masa {
    private int id,masaNo;
    
    
    public Masa(int id, int masaNo) {
        this.id = id;
        this.masaNo = masaNo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMasaNo() {
        return masaNo;
    }

    public void setMasaNo(int masaNo) {
        this.masaNo = masaNo;
    }


    
}
