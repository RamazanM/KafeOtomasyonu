public class Adisyon {
    int masa_no;
    float toplam;

    public Adisyon(int masa_no, float toplam) {
        this.masa_no = masa_no;
        this.toplam = toplam;
    }

    public int getMasa_no() {
        return masa_no;
    }

    public void setMasa_no(int masa_no) {
        this.masa_no = masa_no;
    }

    public float getToplam() {
        return toplam;
    }

    public void setToplam(float toplam) {
        this.toplam = toplam;
    }

    
    
    
    
}
